public class Main {
    public static void main(String[] args) {
//    Operadores de comparacao
//      ==, >=, <=, !=, >, <
//      &&, ||, !variavel

        int numero1 = 20;
        int numero2 = 220;
        if (numero1 > numero2) {
            System.out.println("Numero 1 é maior");
        } else if (numero1 == numero2) {
            System.out.println("Numero 1 é igual ao número 2");
        } else {
            System.out.println("Número 2 é maior que o número 1");
        }
    }
}