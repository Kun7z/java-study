public class Main {
    public static void main(String[] args) {
        int numero = 20;

        switch (numero) {
            case 10:
                System.out.println("Número é 10.");
                break;
            case 11:
                System.out.println("O número é 11.");
                break;
            default:
                System.out.println("Não está dentre os números conhecidos.");
        }
    }
}